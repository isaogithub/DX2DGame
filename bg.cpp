BG* GetBG(void)
{
	return &g_BG;
}

void SetBGSFrame(float MaxX,float MaxY,float MaxFrame)
{
	if (g_Scroll == TRUE)return;

	g_Scroll = TRUE;
	g_MaxX = MaxX;
	g_MaxY = MaxY;
	g_BGSFlame = MaxFrame;
	g_frameCnt = MaxFrame;
	g_CheckPointNum++;

	return;
}
void BGScrool(void)
{
	if (g_Scroll == FALSE)return;

	if (g_frameCnt-- > 0)
	{
		float scrollx = g_MaxX / g_BGSFlame;
		float scrolly = g_MaxY / g_BGSFlame;

		g_BG.pos.x += scrollx;
		g_BG.pos.y += scrolly;
	}
	else
	{
		g_frameCnt = 60;
		g_Scroll = FALSE;
	}

	return;
}

