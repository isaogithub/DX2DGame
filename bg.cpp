//=============================================================================
// 初期化処理
//=============================================================================
HRESULT InitBG(void)
{
	g_Scroll = FALSE;

	g_BGSFlame = 0;
	g_frameCnt = 0;
	g_MaxX = 0.0f;
	g_MaxY = 0.0f;
	g_CheckPointNum = 0;
	g_shaking = FALSE;
	ID3D11Device *pDevice = GetDevice();
	//テクスチャ生成
	for (int i = 0; i < TEXTURE_MAX; i++)
	{
		g_Texture[i] = NULL;
		D3DX11CreateShaderResourceViewFromFile(GetDevice(),
			g_TexturName[i],
			NULL,
			NULL,
			&g_Texture[i],
			NULL);
	}

	// 頂点バッファ生成
	D3D11_BUFFER_DESC bd;
	ZeroMemory(&bd, sizeof(bd));
	bd.Usage = D3D11_USAGE_DYNAMIC;
	bd.ByteWidth = sizeof(VERTEX_3D) * 4;
	bd.BindFlags = D3D11_BIND_VERTEX_BUFFER;
	bd.CPUAccessFlags = D3D11_CPU_ACCESS_WRITE;
	GetDevice()->CreateBuffer(&bd, NULL, &g_VertexBuffer);


	switch (GetMode())
	{
	case MODE_TUTORIAL:
		

		// 変数の初期化
		g_BG.w = 3840.0f;
		g_BG.h = 1080.0f;
		g_BG.pos = XMFLOAT3(0.0f, 0.0f, 0.0f);
		g_BG.texNo = 0;

		g_BG.scrl = 0.0f;		// TEXスクロール
		g_BG.scrl2 = 0.0f;		// TEXスクロール

		break;

	case MODE_GAME:
		
		// 変数の初期化
		g_BG.w = 4000.0f;
		g_BG.h = 3500.0f;
		g_BG.pos = XMFLOAT3(0.0f, 80.0f, 0.0f);
		g_BG.texNo = 1;

		g_BG.scrl = 0.0f;		// TEXスクロール
		g_BG.scrl2 = 0.0f;		// TEXスクロール
		break;

	case MODE_BOSS:
		
		// 変数の初期化
		g_BG.w = SCREEN_WIDTH;
		g_BG.h = SCREEN_HEIGHT;
		g_BG.pos = XMFLOAT3(0.0f, 0.0f, 0.0f);
		g_BG.texNo = 2;

		g_BG.scrl = 0.0f;		// TEXスクロール
		g_BG.scrl2 = 0.0f;		// TEXスクロール
		break;
	}

	


	g_Load = TRUE;
	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void UninitBG(void)
{
	if (g_Load == FALSE) return;

	if (g_VertexBuffer)
	{
		g_VertexBuffer->Release();
		g_VertexBuffer = NULL;
	}

	for (int i = 0; i < TEXTURE_MAX; i++)
	{
		if (g_Texture[i])
		{
			g_Texture[i]->Release();
			g_Texture[i] = NULL;
		}
	}

	g_Load = FALSE;
}

